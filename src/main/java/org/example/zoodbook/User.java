package org.example.zoodbook;

public class User {

String name;
String familyName;
String userName;
String password;
String address;
String codeP;
String phNumber;

    public User(String name, String familyName, String userName, String password, String address, String codeP, String phNumber) {
        this.name = name;
        this.familyName = familyName;
        this.userName = userName;
        this.password = password;
        this.address = address;
        this.codeP = codeP;
        this.phNumber = phNumber;
    }
}
